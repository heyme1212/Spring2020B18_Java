package day02_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello");
        System.out.println("\tHello");
        System.out.println("\t\t\tHello");
        System.out.println("Cybertek\nSchool");
        System.out.println("\nMy\nname\nis\nHilal");

        // \n means new line, starts a new line
        System.out.println("My favorite TV show is \'Game of Thrones\"");
        // \"means double quote

        System.out.println("\\");
        System.out.println("/");
        System.out.print("My favorite book is 'Java'");
    }
}
